# Найти скалярное произведение векторов x,y∈R:
# а) x=(0,−3,6), y=(−4,7,9);
# б) x=(7,−4,0,1), y=(−3,1,11,2).

import numpy as np

x = np.array([0, -3, 6])
y = np.array([-4, 7, 9])
x1 = np.array([7, -4, 0, 1])
y1 = np.array([-3, 1, 11, 2])

print('а)', np.dot(x, y))  # 0*(-4) + (-3)*7 + 6*9
print('б)', np.dot(x1, y1))  # 7*(-3) + (−4)*1 + 0*11 + 1*2
